function func()
{
	$('#myModal').css('display','block');
}
$(document).ready(function()
{
	$('#close').click(function(){
		$('#myModal').css('display','none');
	});
	$('#span').click(function(){
		$('#myModal').css('display','none');
	})
});
window.onclick=function(event)
{
	if(event.target.id==='myModal')
	{
		$('#myModal').css('display','none');
	}
}